#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	size_t j;

	j = ft_strlen(s1);
	while (n-- && *s2 != '\0')
	{
		s1[j++] = *s2;
		s2++;
	}
	s1[j] = '\0';
	return (s1);
}
